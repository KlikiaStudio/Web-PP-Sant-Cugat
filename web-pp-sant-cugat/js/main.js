const LEER_MAS = { es:"Leer más →", ca:"Llegir més →" };

let currentLang = localStorage.getItem('pp-lang') || 'es';

function applyLang(lang){
  currentLang = lang;
  localStorage.setItem('pp-lang', lang);
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key = el.getAttribute('data-i18n');
    if(I18N[lang][key] !== undefined) el.textContent = I18N[lang][key];
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el=>{
    const key = el.getAttribute('data-i18n-html');
    if(HTML_KEYS[key]) el.innerHTML = HTML_KEYS[key][lang];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el=>{
    const key = el.getAttribute('data-i18n-placeholder');
    if(I18N[lang][key] !== undefined) el.setAttribute('placeholder', I18N[lang][key]);
  });
  document.querySelectorAll('.lang-switch button').forEach(b=>{
    b.classList.toggle('active', b.getAttribute('data-lang') === lang);
  });
  renderProposals(lang);
  renderVideos(lang);
  renderNews(lang);
}

document.querySelectorAll('.lang-switch button').forEach(btn=>{
  btn.addEventListener('click', ()=> applyLang(btn.getAttribute('data-lang')));
});

/* ==================== RENDER: PROPUESTAS ==================== */
function renderProposals(lang){
  const grid = document.getElementById('proposalsGrid');
  grid.innerHTML = PROPOSALS.map((p,i)=>`
    <div class="card">
      <div class="card-top">
        <div class="card-icon">${p.icon}</div>
        <div class="card-num">0${i+1}</div>
      </div>
      <h3>${p[lang].t}</h3>
      <p>${p[lang].d}</p>
      <a href="#" class="card-link">${LEER_MAS[lang]}</a>
    </div>
  `).join('');
  observeReveal(grid.querySelectorAll('.card'), true);
}

/* ==================== RENDER: VIDEOS ==================== */
function renderVideos(lang){
  const row = document.getElementById('videoRow');
  const data = (window.SITE_VIDEOS || []);
  row.innerHTML = data.map(v=>{
    const title = (v.title && v.title[lang]) || (v.title && v.title.es) || '';
    const badge = v.platform.toUpperCase();
    if(v.platform === 'youtube' && v.youtubeId){
      return `<a class="video-card" href="${v.url}" target="_blank" rel="noopener">
        <span class="video-platform">${badge}</span>
        <iframe class="video-embed" src="https://www.youtube.com/embed/${v.youtubeId}" title="${title}" loading="lazy" allow="encrypted-media" frameborder="0"></iframe>
        <div class="video-info"><div class="vt">${title}</div></div>
      </a>`;
    }
    return `<a class="video-card" href="${v.url}" target="_blank" rel="noopener">
      <span class="video-platform">${badge}</span>
      <div class="video-play">▶</div>
      <div class="video-info"><div class="vt">${title}</div><div class="vh">${v.publishedAt||''}</div></div>
    </a>`;
  }).join('');
  observeReveal(row.querySelectorAll('.video-card'), true);
}

/* ==================== RENDER: NEWS + OPINION ==================== */
function renderNews(lang){
  const news = (window.SITE_NEWS && window.SITE_NEWS.actualidad) || [];
  const opinion = (window.SITE_NEWS && window.SITE_NEWS.opinion) || [];
  const newsList = document.getElementById('newsList');
  newsList.innerHTML = news.map(n=>`
    <div class="news-item">
      <div class="news-thumb">${n.image ? `<img src="${n.image}" alt="">` : ''}</div>
      <div>
        <div class="news-meta"><span>${(n.category&&n.category[lang])||''}</span><span class="dot"></span><span class="date">${formatDate(n.date, lang)}</span></div>
        <h4>${(n.title&&n.title[lang])||''}</h4>
        <p>${(n.excerpt&&n.excerpt[lang])||''}</p>
      </div>
    </div>
  `).join('');
  const opList = document.getElementById('opinionList');
  opList.innerHTML = opinion.map((o,i)=>`
    <div class="op-item">
      <div class="op-num">0${i+1} · EDITORIAL</div>
      <h4>${(o.title&&o.title[lang])||''}</h4>
      <div class="who">${lang==='ca'?'Per':'Por'} ${o.author||''}</div>
    </div>
  `).join('');
  observeReveal(newsList.querySelectorAll('.news-item'), true);
  observeReveal(opList.querySelectorAll('.op-item'), true);
}

function formatDate(iso, lang){
  if(!iso) return '';
  const d = new Date(iso+'T00:00:00');
  return d.toLocaleDateString(lang==='ca'?'ca-ES':'es-ES', {day:'2-digit', month:'short', year:'numeric'});
}

